# Create a function that takes a base number and an exponent number and returns
# the calculation. You should be able to do this with one line in the body of
# your function.

# Write your function here.

def calculate_exponent(num, exp):
    return num ** exp


print(calculate_exponent(5, 5))     #> 3125
print(calculate_exponent(10, 10))   #> 10000000000
print(calculate_exponent(3, 3))     #> 27

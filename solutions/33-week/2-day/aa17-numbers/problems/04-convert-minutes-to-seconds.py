# Write a function that takes an integer that represents a number of minutes and
# converts it to seconds.  You should be able to do this with one line in the
# body of your function.

# Write your function here
def min2sec(min):
    return min * 60

print(min2sec(5)) #> 300
print(min2sec(3)) #> 180
print(min2sec(2)) #> 120

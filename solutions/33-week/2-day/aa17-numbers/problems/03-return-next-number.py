# Create a function that takes a number as an argument, increments the number by
# +1 and returns the result.  You should be able to do this with one line in the
# body of your function.

# Write your function here.
def increment(num):
    return num + 1


print(increment(0))   #> 1
print(increment(9))   #> 10
print(increment(-3))  #> -2
